/*******************************************************************************
  Function:
    void APP_Initialize ( void )
  Remarks:
    See prototype in app.h.
 */

void APP_Initialize(void) {
    /* Place the App state machine in its initial state. */
    appData.state = APP_STATE_INIT;

    /* Device Layer Handle  */
    appData.deviceHandle = USB_DEVICE_HANDLE_INVALID;

    /* Device configured status */
    appData.isConfigured = false;

    /* Initial get line coding state */
    appData.getLineCodingData.dwDTERate = 9600;
    appData.getLineCodingData.bParityType = 0;
    appData.getLineCodingData.bParityType = 0;
    appData.getLineCodingData.bDataBits = 8;

    /* Read Transfer Handle */
    appData.readTransferHandle = USB_DEVICE_CDC_TRANSFER_HANDLE_INVALID;

    /* Write Transfer Handle */
    appData.writeTransferHandle = USB_DEVICE_CDC_TRANSFER_HANDLE_INVALID;

    /* Intialize the read complete flag */
    appData.isReadComplete = true;

    /*Initialize the write complete flag*/
    appData.isWriteComplete = true;

    /* Reset other flags */
    appData.sofEventHasOccurred = false;
    //appData.isSwitchPressed = false;

    /* Set up the read buffer */
    appData.readBuffer = &readBuffer[0];

    /* PUT YOUR LCD, IMU, AND PIN INITIALIZATIONS HERE */

    TRISBbits.TRISB4=1; // B4 i an input
    TRISAbits.TRISA4=0; //A4 is an output
    LATAbits.LATA4=1; //A4 initially set to 3.3V
    SPI1_init();
    initExp();
    LCD_init();
    LCD_clearScreen(0x0000);
    
    startTime = _CP0_GET_COUNT();
}

/******************************************************************************
  Function:
    void APP_Tasks ( void )
  Remarks:
    See prototype in app.h.
 */

void APP_Tasks(void) {
    /* Update the application state machine based
     * on the current state */

        unsigned short bg=0x780F; // background color
    unsigned short charcolor=0x0000; //color of the text
    unsigned short offcolor=0xffff; // color for text background to distinguish b/w off and on pixels
    unsigned short pg_bar_p=0x0000; //progress indicator
    unsigned short pg_bar_n=0x07ff; //progress remaining indicator
    
    unsigned char str [20];
    unsigned char str1 [20]; 
    unsigned char str2 [20];
    unsigned char str3 [20];
    unsigned char str4 [20];
    unsigned char str5 [20];
    
    
    
    unsigned char bar_number_x [4]; //array that is going to contain the number indicating the acc
    
    unsigned char data [30];
    static unsigned int aa = 0;
    
    
        switch (appData.state) {
        case APP_STATE_INIT:

            /* Open the device layer */
            appData.deviceHandle = USB_DEVICE_Open(USB_DEVICE_INDEX_0, DRV_IO_INTENT_READWRITE);

            if (appData.deviceHandle != USB_DEVICE_HANDLE_INVALID) {
                /* Register a callback with device layer to get event notification (for end point 0) */
                USB_DEVICE_EventHandlerSet(appData.deviceHandle, APP_USBDeviceEventHandler, 0);

                appData.state = APP_STATE_WAIT_FOR_CONFIGURATION;
            } else {
                /* The Device Layer is not ready to be opened. We should try
                 * again later. */
            }

            break;

        case APP_STATE_WAIT_FOR_CONFIGURATION:

            /* Check if the device was configured */
            if (appData.isConfigured) {
                /* If the device is configured then lets start reading */
                appData.state = APP_STATE_SCHEDULE_READ;
            }
            break;

        case APP_STATE_SCHEDULE_READ:

            if (APP_StateReset()) {
                break;
            }

            /* If a read is complete, then schedule a read
             * else wait for the current read to complete */

            appData.state = APP_STATE_WAIT_FOR_READ_COMPLETE;
            if (appData.isReadComplete == true) {
                appData.isReadComplete = false;
                appData.readTransferHandle = USB_DEVICE_CDC_TRANSFER_HANDLE_INVALID;

                USB_DEVICE_CDC_Read(USB_DEVICE_CDC_INDEX_0,
                        &appData.readTransferHandle, appData.readBuffer,
                        APP_READ_BUFFER_SIZE);

                        if (appData.readBuffer[0] == 'r') { //if the letter r is read by the computer
                    aa = 1;
                        }

                if (appData.readTransferHandle == USB_DEVICE_CDC_TRANSFER_HANDLE_INVALID) {
                    appData.state = APP_STATE_ERROR;
                    break;
                }
            }

            break;

        case APP_STATE_WAIT_FOR_READ_COMPLETE:
        case APP_STATE_CHECK_TIMER:

            if (APP_StateReset()) {
                break;
            }

            /* Check if a character was received or a switch was pressed.
             * The isReadComplete flag gets updated in the CDC event handler. */

             /* WAIT FOR 5HZ TO PASS OR UNTIL A LETTER IS RECEIVED */
            if (appData.isReadComplete || _CP0_GET_COUNT() - startTime > (48000000 / 2 / 5)) {
                appData.state = APP_STATE_SCHEDULE_WRITE;
            }


            break;
    


        case APP_STATE_SCHEDULE_WRITE:

            if (APP_StateReset()) {
                break;
            }

            /* Setup the write */

            appData.writeTransferHandle = USB_DEVICE_CDC_TRANSFER_HANDLE_INVALID;
            appData.isWriteComplete = false;
            appData.state = APP_STATE_WAIT_FOR_WRITE_COMPLETE;

            /* PUT THE TEXT YOU WANT TO SEND TO THE COMPUTER IN dataOut
            AND REMEMBER THE NUMBER OF CHARACTERS IN len */
            /* THIS IS WHERE YOU CAN READ YOUR IMU, PRINT TO THE LCD, ETC */
            
            
if (aa == 1) {
                 read_multiple_i2c(0b1101011,0x20,data,14); //read the seven consecutive registers indicating temperature, acceleration in xyz, and angular velocity in xyz

                //signed short temp = data[1]<< 8 | data [0]; //temperature is given by two 8 bits number, store the second one, then shift on by 8 bits and store the first one
                signed short gyro_x = data[3] << 8 | data [2];
                signed short gyro_y = data[5] << 8 | data [4];
                signed short gyro_z = data[7] << 8 | data [6];
                signed short acc_x = data[9] << 8 | data [8];
                signed short acc_y = data[11] << 8 | data [10];
                signed short acc_z = data[13] << 8 | data [12];
                signed short maf_z = 0;
                signed short fir_z = 0;
                signed short iir_z = 0;
                

                //sprintf(message1, "%d" , temp);  

                //writeString(10, 10, message1, WHITE, BLACK); //write temp starting from pixel at x=28 y=32
                //print values to LCD
             sprintf(str2, "Ax %d   " , acc_x); 
             LCD_drawstring(str2,20,30,charcolor,offcolor);  
         
            sprintf(str3, "Ay %d " , acc_y);    
            LCD_drawstring(str3,20,40,charcolor,offcolor);  
         
            sprintf(str4, "Gx %d    " , gyro_x);    
        
            LCD_drawstring(str4,20,50,charcolor,offcolor);  
             sprintf(str5, "Gy %d    " , gyro_y); 
              LCD_drawstring(str5,20,60,charcolor,offcolor);  
                
                //Write values for filters:

                    //MAF filter
                a_z[i]=acc_z;//buffer acceleration
                
                if (i<=4){//at beginning cant average so set to raw values 
                maf_z=a_z[i];
                }
                else{//then start averaging
                    maf_z=(a_z[i-4]+a_z[i-3]+a_z[i-2]+a_z[i-1]+a_z[i])/5;
                } 
              
                
                //IIR filter
                iir_z=0.5*old_acc_z+0.5*acc_z;
                old_acc_z=acc_z;
                
                
                //FIR filter                
                if(i<=3){//first five times just set filter to same value as acceleration (lazy option)
                    fir_z=a_z[i];
                }
                else{//then start averaging
                fir_z=(a_z[i]*b1+a_z[i-1]*b2+a_z[i-2]*b3+a_z[i-3]*b4);
                }
                
                //output to computer screen
                len = sprintf(dataOut, "%d AzRAW %d AzMAF %d AzFIR %d AzIIR %d \r\n", i, acc_z, maf_z, fir_z, iir_z);
               
            i++;
                }
                
            else {
                len = 1;
                dataOut[0] = 0;
            }

             if (i > 100) {//stop after 100 values have been printed to the screen 
                aa = 0; //reset the variable that indicates if 'r' has been pressed to 'off'
                i=0; //reset the counting variable
                old_acc_z=0;//reset old acceleration
                int k=0;
                for(k=0;k<100;k++){
                a_z[k]=0; //reinitialize buffer array to zero
                 }
                    }           
            
            // increment the index so we see a change in the text
            /* IF A LETTER WAS RECEIVED, ECHO IT BACK SO THE USER CAN SEE IT */
            if (appData.isReadComplete) {
                USB_DEVICE_CDC_Write(USB_DEVICE_CDC_INDEX_0,
                        &appData.writeTransferHandle,
                        appData.readBuffer, 1,
                        USB_DEVICE_CDC_TRANSFER_FLAGS_DATA_COMPLETE);
            }
            /* ELSE SEND THE MESSAGE YOU WANTED TO SEND */
            else {
                USB_DEVICE_CDC_Write(USB_DEVICE_CDC_INDEX_0,
                        &appData.writeTransferHandle, dataOut, len,
                        USB_DEVICE_CDC_TRANSFER_FLAGS_DATA_COMPLETE);
                startTime = _CP0_GET_COUNT(); // reset the timer for acurate delays
            }
            break;

        case APP_STATE_WAIT_FOR_WRITE_COMPLETE:

            if (APP_StateReset()) {
                break;
            }

            /* Check if a character was sent. The isWriteComplete
             * flag gets updated in the CDC event handler */

            if (appData.isWriteComplete == true) {
                appData.state = APP_STATE_SCHEDULE_READ;
            }

            break;

        case APP_STATE_ERROR:
            break;
        default:
            break;
    }
}



